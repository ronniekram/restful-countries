import { useContext } from 'react';
import Head from 'next/head';

import Search from '../components/Search';
import Filter from '../components/Filter';
import Countries from '../components/Countries';

import CountriesContext from '../store/countries-context';

import styles from '../styles/pages/index.module.css';

const Home = () => {
  const countriesCtx = useContext(CountriesContext);
  const { selected, query, filtered, setSelected, setQuery, filterCountries, searchCountries } = countriesCtx;


  return (
    <>
      <Head>
        <title>RESTful Countries</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
        <section className={styles.deskQueries}>
          <Search
            query={query}
            setQuery={setQuery}
            searchCountries={searchCountries}
          />

          <Filter
            filterCountries={filterCountries}
            selected={selected}
            setSelected={setSelected}
          />
        </section>

        <section className={styles.countryContainer}>
          <Countries countries={filtered} />
        </section>
    </>
  );
};

export default Home;
